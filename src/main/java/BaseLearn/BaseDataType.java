package BaseLearn;

public class BaseDataType {
    public static void main(String[] args) {
        int x = 123;
        x += 1;
        byte y = (byte)x;
        System.out.println(Integer.toBinaryString(10));
        String z = String.valueOf(x * 5);
        System.out.println(z);
        System.out.println(y);
    }
}
